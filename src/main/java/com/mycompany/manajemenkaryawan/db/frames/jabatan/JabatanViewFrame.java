    private void ubahButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahButtonActionPerformed
        int barisTerpilih = viewTable.getSelectedRow();
        if(barisTerpilih >= 0){
            TableModel model = viewTable.getModel();
            int id_karyawan = (int) model.getValueAt(barisTerpilih, 0);
            String jabatan = (String) model.getValueAt(barisTerpilih,1);
            JabatanAddFrame f = new JabatanAddFrame(id_karyawan,jabatan);
            
            f.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Pilih data");
        }
    }//GEN-LAST:event_ubahButtonActionPerformed

    private void hapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusButtonActionPerformed
        int barisTerpilih = viewTable.getSelectedRow();
        if (barisTerpilih >= 0) {
    int pilihan = JOptionPane.showConfirmDialog(null,
            "Yakin hapus?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

    if (pilihan == 0) {
        try {
            TableModel model = viewTable.getModel();
            int id_karyawan = (int) model.getValueAt(barisTerpilih, 0);
            String jabatan = (String) model.getValueAt(barisTerpilih, 1);
            Connection koneksi = Basisdata.getConnection();
            String deleteSQL = "DELETE FROM jabatan WHERE id_karyawan=" + id_karyawan + " AND jabatan='" + jabatan + "'";
            Statement statement = koneksi.createStatement();
            statement.executeUpdate(deleteSQL);
            koneksi.close();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }
} else {
    JOptionPane.showMessageDialog(null, "Pilih data");
}
    }//GEN-LAST:event_hapusButtonActionPerformed

