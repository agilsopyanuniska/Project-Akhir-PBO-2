    public DepartemenAddFrame(int id_departemen) {
        initComponents();
        idDepartemenTextField.setEnabled(false);
        try {
            Connection koneksi = Basisdata.getConnection();
            String findSQL = "SELECT * FROM departemen WHERE id_departemen="+id_departemen;
            Statement statement = koneksi.createStatement();
            ResultSet resultSet = statement.executeQuery(findSQL);
            while(resultSet.next()){
                idDepartemenTextField.setText(resultSet.getString("id_departemen"));
                namaDepartemenTextField.setText(resultSet.getString("nama_departemen"));
            }
            koneksi.close();
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }
    }

